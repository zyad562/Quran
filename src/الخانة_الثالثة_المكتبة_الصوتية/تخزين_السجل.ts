/**
 * خدمة تخزين سجل الاستماع (Recent Plays Storage Service)
 * مسؤولة عن حفظ واسترجاع آخر السور التي تم الاستماع إليها
 */

import { قارئ, سورة_صوتية } from './قائمة_السور_114';

export interface سورة_مستمعة {
  سورة: سورة_صوتية;
  قارئ: قارئ;
  تاريخ_الاستماع: number;
}

const مفتاح_السجل = 'نور_الإسلام_سجل_الاستماع';
const الحد_الأقصى = 15;

export const تخزين_السجل = {
  /**
   * جلب سجل الاستماع الأخير
   */
  جلب_الكل: (): سورة_مستمعة[] => {
    try {
      const بيانات = localStorage.getItem(مفتاح_السجل);
      return بيانات ? JSON.parse(بيانات) : [];
    } catch (error) {
      console.error('خطأ في جلب سجل الاستماع:', error);
      return [];
    }
  },

  /**
   * إضافة سورة إلى سجل الاستماع
   */
  إضافة_للسجل: (سورة: سورة_صوتية, قارئ: قارئ) => {
    try {
      const الكل = تخزين_السجل.جلب_الكل();
      const معرف_فريد = `${قارئ.id}_${سورة.id}`;
      
      // إزالة السورة إذا كانت موجودة مسبقاً لوضعها في البداية
      const قائمة_منقحة = الكل.filter(item => `${item.قارئ.id}_${item.سورة.id}` !== معرف_فريد);
      
      const قائمة_جديدة = [
        { سورة, قارئ, تاريخ_الاستماع: Date.now() },
        ...قائمة_منقحة
      ].slice(0, الحد_الأقصى);

      localStorage.setItem(مفتاح_السجل, JSON.stringify(قائمة_جديدة));
      
      // إرسال حدث مخصص لتنبيه المكونات بتحديث السجل
      window.dispatchEvent(new CustomEvent('تحديث_سجل_الاستماع'));
    } catch (error) {
      console.error('خطأ في تحديث سجل الاستماع:', error);
    }
  }
};
