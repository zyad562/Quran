/**
 * خدمة تخزين المفضلة (Favorites Storage Service)
 * مسؤولة عن حفظ واسترجاع السور المفضلة
 */

import { قارئ, سورة_صوتية } from './قائمة_السور_114';

export interface سورة_مفضلة {
  سورة: سورة_صوتية;
  قارئ: قارئ;
  تاريخ_الإضافة: number;
}

const مفتاح_المفضلة = 'نور_الإسلام_المفضلة_الصوتية';

export const تخزين_المفضلة = {
  /**
   * جلب كافة السور المفضلة
   */
  جلب_الكل: (): سورة_مفضلة[] => {
    try {
      const بيانات = localStorage.getItem(مفتاح_المفضلة);
      return بيانات ? JSON.parse(بيانات) : [];
    } catch (error) {
      console.error('خطأ في جلب المفضلة:', error);
      return [];
    }
  },

  /**
   * إضافة أو إزالة سورة من المفضلة
   */
  تبديل_المفضلة: (سورة: سورة_صوتية, قارئ: قارئ) => {
    try {
      const الكل = تخزين_المفضلة.جلب_الكل();
      const معرف_فريد = `${قارئ.id}_${سورة.id}`;
      
      const موجود = الكل.find(item => `${item.قارئ.id}_${item.سورة.id}` === معرف_فريد);
      
      let قائمة_جديدة;
      if (موجود) {
        قائمة_جديدة = الكل.filter(item => `${item.قارئ.id}_${item.سورة.id}` !== معرف_فريد);
      } else {
        قائمة_جديدة = [
          { سورة, قارئ, تاريخ_الإضافة: Date.now() },
          ...الكل
        ];
      }

      localStorage.setItem(مفتاح_المفضلة, JSON.stringify(قائمة_جديدة));
      
      // إرسال حدث مخصص لتنبيه المكونات بتحديث المفضلة
      window.dispatchEvent(new CustomEvent('تحديث_المفضلة_الصوتية'));
      return !موجود;
    } catch (error) {
      console.error('خطأ في تحديث المفضلة:', error);
      return false;
    }
  },

  /**
   * التحقق مما إذا كانت السورة في المفضلة
   */
  هل_في_المفضلة: (سورة_id: number, قارئ_id: string): boolean => {
    const الكل = تخزين_المفضلة.جلب_الكل();
    return الكل.some(item => item.سورة.id === سورة_id && item.قارئ.id === قارئ_id);
  }
};
